import matplotlib.pyplot as plt
import matplotlib.dates as mdates

sql_tren_wil = f"""
WITH top_wilayah AS (
  SELECT INITCAP(TRIM(district_city)) AS wilayah
  FROM {FULL_TABLE}
  WHERE district_city IS NOT NULL
  GROUP BY wilayah
  ORDER BY COUNT(*) DESC
  LIMIT 10
)
SELECT
  DATE_TRUNC(event_date, MONTH)          AS bulan_dt,
  INITCAP(TRIM(t.district_city))         AS wilayah,
  ROUND(AVG(t.rainfall_mm), 2)           AS rata_curah_hujan
FROM {FULL_TABLE} t
JOIN top_wilayah tw
  ON INITCAP(TRIM(t.district_city)) = tw.wilayah
WHERE t.rainfall_mm IS NOT NULL AND t.rainfall_mm >= 0
GROUP BY bulan_dt, wilayah
ORDER BY bulan_dt, wilayah
"""
df_tren_wil = run_query(sql_tren_wil)
df_tren_wil['bulan_dt'] = pd.to_datetime(df_tren_wil['bulan_dt'])

# KONVERSI TANGGAL
