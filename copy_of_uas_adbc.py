# TREN BULANAN: rata-rata curah hujan per bulan
sql_tren_bulanan = f"""
SELECT
  DATE_TRUNC(event_date, MONTH)          AS bulan_dt,
  FORMAT_DATE('%Y-%m', event_date)       AS year_month,
  COUNT(*)                               AS n_observasi,
  ROUND(AVG(rainfall_mm), 2)             AS rata_curah_hujan,
  ROUND(MAX(rainfall_mm), 2)             AS maks_curah_hujan,
  ROUND(STDDEV(rainfall_mm), 2)          AS std_dev
FROM {FULL_TABLE}
WHERE rainfall_mm IS NOT NULL AND rainfall_mm >= 0
GROUP BY bulan_dt, year_month
ORDER BY bulan_dt
"""
df_tren = run_query(sql_tren_bulanan)
df_tren['bulan_dt'] = pd.to_datetime(df_tren['bulan_dt'])

# konversi tanggal
df_tren['bulan_dt'] = pd.to_datetime(df_tren['bulan_dt'])

# garis tren
x = np.arange(len(df_tren))
z = np.polyfit(x, df_tren['rata_curah_hujan'], 1)

# visualisasi
plt.figure(figsize=(14,5))

plt.plot(
    df_tren['bulan_dt'],
    df_tren['rata_curah_hujan'],
    marker='o',
    label='Rata-rata Bulanan'
)

plt.plot(
    df_tren['bulan_dt'],
    np.poly1d(z)(x),
    linestyle='--',
    label='Tren Linear'
)

plt.gca().xaxis.set_major_formatter(mdates.DateFormatter('%b\n%Y'))

plt.title('Tren Curah Hujan Bulanan Jawa Barat 2020–2025')
plt.xlabel('Waktu')
plt.ylabel('Curah Hujan (mm)')
plt.legend()

plt.tight_layout()
plt.show()

print(f"Slope tren : {z[0]:.4f} mm/bulan")
print(f"Arah tren         : {'📈 NAIK' if z[0]>0 else '📉 TURUN'}")

