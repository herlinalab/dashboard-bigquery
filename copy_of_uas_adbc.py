# pembersihan dan transformasi data
sql_bersih = f"""
WITH deduplicated AS (
  SELECT
    *,
    ROW_NUMBER() OVER (
      PARTITION BY event_id
      ORDER BY event_date
    ) AS rn
  FROM {FULL_TABLE}
  WHERE
    event_id      IS NOT NULL
    AND event_date    IS NOT NULL
    AND district_city IS NOT NULL
    AND rainfall_mm   IS NOT NULL
    AND rainfall_mm   >= 0
)
SELECT
  event_id,
  event_date,
  INITCAP(TRIM(district_city))          AS district_city,
  rainfall_mm,
  -- Kolom turunan
  EXTRACT(YEAR  FROM event_date)        AS tahun,
  EXTRACT(MONTH FROM event_date)        AS bulan,
  FORMAT_DATE('%Y-%m', event_date)      AS year_month,
  DATE_TRUNC(event_date, MONTH)         AS bulan_dt
FROM deduplicated
WHERE rn = 1
ORDER BY event_date
"""

df = run_query(sql_bersih)
df['event_date'] = pd.to_datetime(df['event_date'])
df['bulan_dt']   = pd.to_datetime(df['bulan_dt'])

